const mongoose = require('mongoose');

// Define article schema
const articleSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true
  },
  content: {
    type: String,
    required: true
  },
  tags: [String],
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
});

// Create Article model
const Article = mongoose.model('Article', articleSchema);

module.exports = Article;
